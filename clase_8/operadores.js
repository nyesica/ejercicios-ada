//Operadores aritméticos

let a = 10;
let b = 2;
console.log(a+b);// suma: 12
console.log(a-b); //Resta: 8
console.log(a*b); //Multiplicación: 20
console.log(a/b); //División: 5
console.log(a%b); //Módulo: 0

//Operadores de comparación
console.log(a>b); //true
console.log(a<b); //false
console.log(a==="10"); //false

//Operadores lógicos
console.log(true && false); //me devuelve solo true si ambos valores son true, entonces es falso

